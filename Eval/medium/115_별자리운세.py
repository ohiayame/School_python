# 별자리 경계 데이터: (이름, 시작월, 시작일, 종료월, 종료일)
# 염소자리(12/22~1/19)처럼 연도를 넘는 경우는 아래 별도 처리
sign_ranges = [
    ("물병자리",  1, 20,  2, 18),
    ("물고기자리", 2, 19,  3, 20),
    ("양자리",    3, 21,  4, 19),
    ("황소자리",  4, 20,  5, 20),
    ("쌍둥이자리", 5, 21,  6, 21),
    ("게자리",    6, 22,  7, 22),
    ("사자자리",  7, 23,  8, 22),
    ("처녀자리",  8, 23,  9, 22),
    ("천칭자리",  9, 23, 10, 22),
    ("전갈자리", 10, 23, 11, 21),
    ("사수자리", 11, 22, 12, 21),
]

# 반복문으로 별자리 판별 (데이터 기반 접근)
for name, start_m, start_d, end_m, end_d in sign_ranges:
    if (month == start_m and day >= start_d) or (month == end_m and day <= end_d):
        sign = name
        break

# 염소자리(12/22~1/19)는 연도 경계를 넘으므로 별도 처리
if not sign:
    if (month == 12 and day >= 22) or (month == 1 and day <= 19):
        sign = "염소자리"

print(f"당신의 별자리는 {sign}입니다!")
print(f"오늘의 운세: {fortune_by_sign[sign]}")