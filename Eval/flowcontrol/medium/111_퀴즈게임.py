# 문제마다 출력 후 input, 채점, 점수 누적
score = 0

# 문제 데이터를 튜플 리스트로 응집 — 수정·추가 시 한 곳만 변경하면 됩니다
questions = [
    (12,  8,  20),  # 문제 1
    (35, 17,  52),  # 문제 2
    (99,  1, 100),  # 문제 3
    (46, 27,  73),  # 문제 4
    (58, 34,  92),  # 문제 5
]

# 5개의 덧셈 문제를 순서대로 출제
for num, (a, b, ans) in enumerate(questions, 1):
    # 문제 K: A + B = ?를 출력
    print(f"문제 {num}: {a} + {b} = ?")

    # 입력 받기
    user_answer = int(input())  # 변수명을 역할 중심으로 명명

    # 정답! 출력 / 맞을 때마다 20점씩 적립
    if user_answer == ans:
        print("정답!")
        score += 20
    # 틀리면 오답! 정답은 X입니다 를 출력
    else:
        print(f"오답! 정답은 {ans}입니다")

# 마지막에 === 최종 점수: S / 100 ===를 출력
print("=== 최종 점수:", score, "/ 100 ===")