# if/elif/else + or 연산자를 사용하세요.
month = int(input())

# 3, 4, 5월: 봄입니다. / 따뜻한 봄이에요! 꽃구경 가세요 🌸
if 3 <= month <= 5:
    print("봄입니다.\n따뜻한 봄이에요! 꽃구경 가세요 🌸")
# 6, 7, 8월: 여름입니다. / 더운 여름이에요! 시원하게 보내세요 🌊
elif 6 <= month <= 8:
    print("여름입니다.\n더운 여름이에요! 시원하게 보내세요 🌊")
# 9, 10, 11월: 가을입니다. / 선선한 가을이에요! 단풍 구경 가세요 🍂
elif 9 <= month <= 11:
    print("가을입니다.\n선선한 가을이에요! 단풍 구경 가세요 🍂")
# 12, 1, 2월: 겨울입니다. / 추운 겨울이에요! 따뜻하게 입으세요 ⛄
elif month == 12 or month == 1 or month == 2:
    print("겨울입니다.\n추운 겨울이에요! 따뜻하게 입으세요 ⛄")
# 그 외: 잘못된 입력입니다. (한 줄만)
else:
    print("잘못된 입력입니다.")