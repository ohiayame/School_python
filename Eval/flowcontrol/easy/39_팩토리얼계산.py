"""
[문제]
    - 숫자 N을 입력받아 N!(팩토리얼)을 N! = 값 형식으로 출력하시오.
    - N!은 1부터 N까지의 모든 수의 곱입니다.
    - (print(str(n) + "! =", result) 사용 — ! 와 = 사이에 공백 하나)
    - 누적 곱 변수의 초깃값은 1로 두세요.
"""

n = int(input())
# 문제에 정의되어있는 변수명 result 사용하여 팩토리얼 초기화
result = 1

# 1부터 N까지 반복하여 모든 수를 곱하기
for i in range(1, n + 1):
    result *= i

# 문제에 지정되어있는 방식으로 출력
print(str(n) + "! =", result)