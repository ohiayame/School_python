# 컴퓨터는 "바위" 고정. 사용자 입력과 비교하여 결과를 출력하세요.
computer = "바위"
user = input()

# 입력에 따른 출력 형식
# arr = {"바위" : "비겼습니다", 
#        "가위" : "졌습니다",
#        "보" : "이겼습니다"}
result_map = {
    "바위": "비겼습니다",
    "가위": "졌습니다",
    "보": "이겼습니다",
}

# 결과 값 
# arr에서 사용자 입력값에 대한 출력값을 찾기
# (arr에 없는 입력의 경우 "잘못된 입력입니다" 출력)
result = result_map.get(user, "잘못된 입력입니다")

# 출력
print(f"컴퓨터: {computer}")
print(f"나: {user}")
print(f"결과: {result}")